package profiles.api.services;

public class Login {

    private String login;
    private String password;

    public void setLogin(String str) {
        login = str;
    }

    public String getLogin() {
        return login;
    }

    public void setPassword(String str) {
        password = str;
    }

    public String getPassword() {
        return password;
    }

}
